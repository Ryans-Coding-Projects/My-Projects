#This program allows the user to enter a number and returns whether its odd or even

while True:

    num = int(input("\nEnter a number: "))
    
    if num % 2 == 0:
        print("\n" , num , "is even")
        
    
    elif num % 2 != 0:
        print("\n", num, "is odd")
    

   

    
        
    

